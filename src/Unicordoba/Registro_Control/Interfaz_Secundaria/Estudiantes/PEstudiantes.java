    private void CargarSede() {
        factory = Persistence.createEntityManagerFactory("R-C_Unicor_LoricaPU");
        List<Sede> list = new SedeJpaController(factory).findSedeEntities();
        for (Sede list1 : list) {
            cSedes.addItem(list1);
        }
    }
