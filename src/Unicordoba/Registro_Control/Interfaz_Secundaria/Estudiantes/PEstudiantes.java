    @Override
    public void Nuevo() {
        informacionBasica = new InformacionBasica();
        informacionDeSeguridad = new InformacionDeSeguridad();
        estudiante = new Estudiante();
    }

    @Override
    public void Seleccionar(Vector vectorSeleccion) {
     factory = Persistence.createEntityManagerFactory("R-C_Unicor_LoricaPU");
        EstudianteJpaController controller = new EstudianteJpaController(factory);

        this.estudiante = controller.findEstudiante(Integer.valueOf(vectorSeleccion.get(0).toString()));
        this.informacionBasica = estudiante.getInformacionBasicaId();
        this.informacionDeSeguridad = estudiante.getInformacionDeSeguridadidI();

        lId.setText(estudiante.getIdEstudiante().toString());
        tNombres.setText(estudiante.getInformacionBasicaId().getNombres());
        tApellidos.setText(estudiante.getInformacionBasicaId().getApellidos());
        tCondigo.setText(estudiante.getInformacionBasicaId().getCodigo());
        tCorreo.setText(estudiante.getInformacionBasicaId().getCorreo());
        tIdentificacion.setText(estudiante.getInformacionBasicaId().getTiCc() + "");
        tTelefono.setText(estudiante.getInformacionBasicaId().getTelefono() + "");

        lCH.setText(estudiante.getInformacionDeSeguridadidI().getCodigoHuella());
        tClave.setText(estudiante.getInformacionDeSeguridadidI().getClave());
        cEstado.setSelectedItem(estudiante.getInformacionDeSeguridadidI().getEstado());

        cSedes.setSelectedItem(estudiante.getSedeid());
    
    }

    @Override
    public List<Object[]> getListaParaTabla() {
        factory = Persistence.createEntityManagerFactory("R-C_Unicor_LoricaPU");
        EstudianteJpaController controller = new EstudianteJpaController(factory);

        List<Object[]> list = new ArrayList();
        for (Estudiante estudiante : controller.findEstudianteEntities()) {
            list.add(new Object[]{
                estudiante.getIdEstudiante(),
                estudiante.getInformacionBasicaId().getNombres(),
                estudiante.getInformacionBasicaId().getApellidos()
            });
        }
        return list;
    
    }

