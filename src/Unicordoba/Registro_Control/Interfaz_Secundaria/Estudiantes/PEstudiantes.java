    private EntityManagerFactory factory;
    private InformacionBasica informacionBasica = null;
    private InformacionDeSeguridad informacionDeSeguridad = null;
    private Estudiante estudiante = null;
    @Override
    public void Guardar(Estado_Ventana estado_Ventana) {
        try {
      
        factory = Persistence.createEntityManagerFactory("R-C_Unicor_LoricaPU");
      
         informacionBasica.setNombres(tNombres.getText());
         informacionBasica.setApellidos(tApellidos.getText());
         informacionBasica.setCodigo(tCondigo.getText());
         informacionBasica.setCorreo(tCorreo.getText());
         informacionBasica.setTelefono(tTelefono.getText());
         informacionBasica.setTiCc(Integer.valueOf(tIdentificacion.getText()));
      
        InformacionBasicaJpaController informacionBasicaJpaController = new InformacionBasicaJpaController(factory);
         
        if (estado_Ventana.equals(Estado_Ventana.NUEVO)) {
                informacionBasicaJpaController.create(informacionBasica);
            } else {
                informacionBasicaJpaController.edit(informacionBasica);
            }
        
        
        informacionDeSeguridad.setClave(tClave.getText());
        informacionDeSeguridad.setCodigoHuella(lCH.getText());
        informacionDeSeguridad.setEstado(cEstado.getSelectedItem().toString());
        
        InformacionDeSeguridadJpaController informacionDeSeguridadJpaController = new InformacionDeSeguridadJpaController(factory);
        
        if (estado_Ventana.equals(Estado_Ventana.NUEVO)) {
                informacionDeSeguridadJpaController.create(informacionDeSeguridad);
            } else {
                informacionDeSeguridadJpaController.edit(informacionDeSeguridad);
            }
        
            estudiante.setInformacionBasicaId(informacionBasica);
            estudiante.setInformacionDeSeguridadidI(informacionDeSeguridad);
            estudiante.setSedeid((Sede) cSedes.getSelectedItem());
            EstudianteJpaController controller = new EstudianteJpaController(factory);
         
        if (estado_Ventana.equals(Estado_Ventana.NUEVO)) {
                controller.create(estudiante);
            } else {
                controller.edit(estudiante);
            }

        } catch (Exception ex) {

            ex.printStackTrace();
        }
        
    }

    @Override
    public void Eliminar() {
        
     try {
            factory = Persistence.createEntityManagerFactory("R-C_Unicor_LoricaPU");

            EstudianteJpaController controller = new EstudianteJpaController(factory);
            controller.destroy(estudiante.getIdEstudiante());
            
            InformacionBasicaJpaController basicaJpaController = new InformacionBasicaJpaController(factory);
            basicaJpaController.destroy(estudiante.getInformacionBasicaId().getId());

            InformacionDeSeguridadJpaController deSeguridadJpaController = new InformacionDeSeguridadJpaController(factory);
            deSeguridadJpaController.destroy(estudiante.getInformacionDeSeguridadidI().getIdI());

        } catch (NonexistentEntityException ex) {
            ex.printStackTrace();
        } catch (IllegalOrphanException ex) {
            ex.printStackTrace();
        }
    }

